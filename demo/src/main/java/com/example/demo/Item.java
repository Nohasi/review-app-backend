package com.example.demo;

public class Item {
    private Integer itemId;
    private String itemName;
    private String itemImage;
    private Double itemPrice;
    private String itemBrand;
    private Integer itemButtons;
    private Integer itemAnalog;
    private String compatibleSystems;
    public Integer getItemId() {
        return itemId;
    }
    public void setItemId(Integer itemId) {
        this.itemId = itemId;
    }
    public String getItemName() {
        return itemName;
    }
    public void setItemName(String itemName) {
        this.itemName = itemName;
    }
    public String getItemImage() {
        return itemImage;
    }
    public void setItemImage(String itemImage) {
        this.itemImage = itemImage;
    }
    public Double getItemPrice() {
        return itemPrice;
    }
    public void setItemPrice(Double itemPrice) {
        this.itemPrice = itemPrice;
    }
    public String getItemBrand() {
        return itemBrand;
    }
    public void setItemBrand(String itemBrand) {
        this.itemBrand = itemBrand;
    }
    public Integer getItemButtons() {
        return itemButtons;
    }
    public void setItemButtons(Integer itemButtons) {
        this.itemButtons = itemButtons;
    }
    public Integer getItemAnalog() {
        return itemAnalog;
    }
    public void setItemAnalog(Integer itemAnalog) {
        this.itemAnalog = itemAnalog;
    }
    public String getCompatibleSystems() {
        return compatibleSystems;
    }
    public void setCompatibleSystems(String compatibleSystems) {
        this.compatibleSystems = compatibleSystems;
    }
}
